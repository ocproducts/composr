    /**
     * The UI to confirm sending of our newsletter.
     *
     * @return Tempcode The UI
     */
    public function confirm_send()
    {
        // Read in details
        $message = post_param_string('message');
        $subject = post_param_string('subject');
        $lang = choose_language($this->title);
        $template = post_param_string('template', 'MAIL');
        $in_full = post_param_integer('in_full', 0);
        $html_only = post_param_integer('html_only', 0);
        $from_email = post_param_string('from_email', '');
        $from_name = post_param_string('from_name', '');
        $address = $GLOBALS['FORUM_DRIVER']->get_member_email_address(get_member());
        if ($address == '') {
            $address = get_option('staff_address');
        }
        $username = $GLOBALS['FORUM_DRIVER']->get_username(get_member(), true);

        // Read in CSV target
        $extra_post_data = array();
        require_code('uploads');
        $_csv_data = post_param_string('csv_data', null);
        if ($_csv_data !== null) {
            $extra_post_data['csv_data'] = $_csv_data;
        } else {
            if (((is_plupload(true)) && (array_key_exists('file', $_FILES))) || ((array_key_exists('file', $_FILES)) && (is_uploaded_file($_FILES['file']['tmp_name'])))) {
                $__csv_data = array();
                cms_ini_set('auto_detect_line_endings', '1'); // TODO: Remove with #3032
                $myfile = fopen($_FILES['file']['tmp_name'], 'rb');
                // TODO: #3032
                $del = ',';
                $csv_test_line = fgetcsv($myfile, 4096, $del);
                if ((count($csv_test_line) == 1) && (strpos($csv_test_line[0], ';') !== false)) {
                    $del = ';';
                }
                rewind($myfile);
                while (($csv_line = fgetcsv($myfile, 4096, $del)) !== false) {
                    $__csv_data[] = $csv_line;
                }
                fclose($myfile);

                $extra_post_data['csv_data'] = json_encode($__csv_data);
            }
        }

        // Periodic save?
        if (post_param_integer('make_periodic', 0) == 1) {
            // We're making a periodic newsletter. Thus we need to pass this info
            // through to the next step
            $extra_post_data['make_periodic'] = '1';

            // Re-generate preview from latest chosen_categories
            $message = generate_whatsnew_comcode(post_param_string('chosen_categories', ''), $in_full, $lang, post_param_date('cutoff'));
            if ($message === null) {
                $message = do_lang('NO_ENTRIES');
            }
        }

        // Render
        list($html_version, $text_version, $in_html) = newsletter_preview($message, $subject, $html_only == 1, null, null, null, null, null, null, $template);

        // Subject line
        $_full_subject = $subject;
        if (post_param_integer('make_periodic', 0) == 1) {
            $_full_subject .= ' - ' . get_timezoned_date(time(), false);
        }
        $full_subject = do_lang('NEWSLETTER_PREVIEW_SUBJECT', $_full_subject);

        // Send e-mail
        require_code('mail');
        $mail_dispatcher = dispatch_mail(
            $full_subject,
            ($html_only == 1) ? $html_version->evaluate() : $message,
            array($address),
            $username/*do_lang('NEWSLETTER_SUBSCRIBER',get_site_name())*/,
            $from_email,
            $from_name,
            array(
                'no_cc' => true,
                'as_admin' => true,
                'in_html' => $in_html,
                'mail_template' => $template,
            )
        );

        // Spam check, if possible
        if ($mail_dispatcher->mime_data !== null) {
            require_code('mail');
            require_code('mail2');
            list($spam_report, $spam_score) = email_spam_check($mail_dispatcher->mime_data);
        }

        // Inline preview
        $preview = do_template('NEWSLETTER_CONFIRM_WRAP', array(
            '_GUID' => '02bd5a782620141f8589e647e2c6d90b',
            'SUBJECT' => $full_subject,
