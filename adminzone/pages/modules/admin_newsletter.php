        if (!GOOGLE_APPENGINE) {
            $ret['bounce_filter_a'] = array('BOUNCE_FILTER', 'menu/adminzone/tools/newsletter/newsletter_email_bounce');
        }
        $ret += parent::get_entry_points();
        return $ret;
    }

