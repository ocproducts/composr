        }
    }

    $icon_name = $param[0];
    $icon_id = str_replace('/', '__', $icon_name);

    $value = (strpos($sprite, 'id="icon_' . $icon_id . '"') !== false) ? '1' : '0';

    if ($escaped !== array()) {
        apply_tempcode_escaping($escaped, $value);
    }

    return $value;
}

