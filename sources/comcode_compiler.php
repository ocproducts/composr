                } elseif ($next === '/') {
                    $close = true;
                } else {
                    $current_tag .= strtolower($next);
                    $status = CCP_IN_TAG_NAME;
                }
                break;

            case CCP_IN_TAG_BETWEEN_ATTRIBUTES:
                if ($next === ']') {
                    if ($current_tag === 'title') {
