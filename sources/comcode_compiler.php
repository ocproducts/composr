                    if (!$lax) {
                        return comcode_parse_error($preparse_mode, array('CCP_TAG_OPEN_ANOMALY'), $pos, $comcode, $check_only);
                    }

                    $status = CCP_NO_MANS_LAND;
                    $pos--;
                } elseif ($next === ']') { // Can't actual occur though
