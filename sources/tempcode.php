 * @param  string $in String in
 * @return string Resultant string
 */
function php_addslashes_twice($in)
{
    global $PHP_REP_FROM, $PHP_REP_TO_TWICE;
    return str_replace($PHP_REP_FROM, $PHP_REP_TO_TWICE, $in);
}

