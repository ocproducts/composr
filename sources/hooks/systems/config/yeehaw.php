/**
 * Hook class.
 */
class Hook_config_yeehaw
{
    /**
     * Gets the details relating to the config option.
     *
     * @return ?array The details (null: disabled)
     */
    public function get_details()
    {
        return array(
            'human_name' => 'AMERICAN_ENGLISH',
            'type' => 'tick',
            'category' => 'SITE',
            'group' => 'INTERNATIONALISATION',
            'explanation' => 'CONFIG_OPTION_yeehaw',
            'shared_hosting_restricted' => '0',
            'list_options' => '',
            'order_in_category_group' => 4,

            'required' => true,
            'public' => false,
            'addon' => 'core_configuration',

            'maintenance_code' => 'lang_us',
        );
    }

    /**
     * Gets the default value for the config option.
     *
     * @return ?string The default value (null: option is disabled)
     */
    public function get_default()
    {
        require_code('locations');
        if (geolocate_ip() == 'US') {
            return '1';
        }
        return '0';
    }

    /**
     * Code to run before the option is saved.
     * If there is some kind of problem with the new value then we could attach an error message.
     *
     * @param  string $new_value The new value
     * @param  string $old_value The old value
     * @return boolean Whether to allow the save
     */
    public function presave_handler($new_value, $old_value)
    {
        erase_cached_language();

        return true;
    }
}
