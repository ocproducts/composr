/**
 * Hook class.
 */
class Hook_snippet_count
{
    /**
     * Run function for snippet hooks. Generates XHTML to insert into a page using AJAX.
     *
     * @return Tempcode The snippet
     */
    public function run()
    {
        if (!addon_installed('counting_blocks')) {
            return new Tempcode();
        }

        $name = post_param_string('name');
        update_stat($name, 1);

        return new Tempcode();
    }
}
