/**
 * Hook class.
 */
class Hook_symbol_CART_LINK
{
    /**
     * Run function for symbol hooks. Searches for tasks to perform.
     *
     * @param  array $param Symbol parameters
     * @return string Result
     */
    public function run($param)
    {
        if (!addon_installed('shopping')) {
            return '';
        }

        $value = '';

        require_lang('shopping');

        $cart_url = build_url(array('page' => 'shopping', 'type' => 'browse'), get_module_zone('shopping'));

        $where = array();
        if (is_guest()) {
            $where['session_id'] = get_session_id();
        } else {
            $where['ordered_by'] = get_member();
        }
        $item_count = $GLOBALS['SITE_DB']->query_select_value('shopping_cart', 'COUNT(*)', $where);

        if ($item_count > 0) {
            $title = do_lang_tempcode('BUTTON_CART_ITEMS', strval($item_count));
        } else {
            $title = do_lang_tempcode('BUTTON_CART_EMPTY');
        }

        $_value = do_template('ECOM_CART_LINK', array('_GUID' => '46ae00c8a605b84fee1b1c68fc57cd32', 'URL' => $cart_url, 'ITEMS' => strval($item_count), 'TITLE' => $title));

        $value = $_value->evaluate();
        return $value;
    }
}
