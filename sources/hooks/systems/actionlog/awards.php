/**
 * Hook class.
 */
class Hook_actionlog_awards extends Hook_actionlog
{
    /**
     * Get details of action log entry types handled by this hook.
     *
     * @return array Map of handler data in standard format
     */
    public function get_handlers()
    {
        if (!addon_installed('awards')) {
            return array();
        }

        require_lang('awards');

        return array(
            'ADD_AWARD_TYPE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'award_type',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    'EDIT_THIS_AWARD_TYPE' => '_SEARCH:admin_awards:_edit:{ID}',
                    'ADD_AWARD_TYPE' => '_SEARCH:admin_awards:add',
                ),
            ),
            'EDIT_AWARD_TYPE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'award_type',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    'EDIT_THIS_AWARD_TYPE' => '_SEARCH:admin_awards:_edit:{ID}',
                    'ADD_AWARD_TYPE' => '_SEARCH:admin_awards:add',
                ),
            ),
            'DELETE_AWARD_TYPE' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'award_type',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    'ADD_AWARD_TYPE' => '_SEARCH:admin_awards:add',
                ),
            ),
            'GIVE_AWARD' => array(
                'flags' => ACTIONLOG_FLAGS_NONE,
                'cma_hook' => 'award_type',
                'identifier_index' => 0,
                'written_context_index' => 1,
                'followup_page_links' => array(
                    'VIEW_PAST_WINNERS' => '_SEARCH:awards:award:{ID}',
                    'EDIT_THIS_AWARD_TYPE' => '_SEARCH:admin_awards:_edit:{ID}',
                ),
            ),
        );
    }
}
