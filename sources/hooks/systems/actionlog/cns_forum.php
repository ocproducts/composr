    /**
     * Get written context for an action log entry handled by this hook.
     *
     * @param  array $actionlog_row Action log row
     * @param  array $handler_data Handler data
     * @param  ?string $identifier Identifier (null: none)
     * @return string Written context
     */
    protected function get_written_context($actionlog_row, $handler_data, $identifier)
    {
        switch ($actionlog_row['the_type']) {
            case 'EDIT_POST':
            case 'VALIDATE_POST':
            case 'UNVALIDATE_POST':
            case 'MAKE_ANONYMOUS_POST':
                $member_id = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_posts', 'p_poster', array('id' => intval($actionlog_row['param_a'])));
                if ($member_id !== null) {
                    $username = $GLOBALS['FORUM_DRIVER']->get_username($member_id);
                    if ($username === null) {
                        $username = '#' . strval($member_id);
                    }
                } else {
                    $username = do_lang('UNKNOWN');
                }

                $topic_id = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_posts', 'p_topic_id', array('id' => intval($actionlog_row['param_a'])));
                if ($topic_id !== null) {
                    $topic_title = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_topics', 't_cache_first_title', array('id' => $topic_id));
                    if ($topic_title === null) {
                        $topic_title = '#' . strval($topic_id);
                    }
                } else {
                    $topic_title = do_lang('UNKNOWN');
                }

                $written_context = do_lang('POST_BY_X_IN_Y', $username, $topic_title);
                return $written_context;

            case 'DELETE_POST':
                $topic_id = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_posts', 'p_topic_id', array('id' => intval($actionlog_row['param_a'])));
                if ($topic_id !== null) {
                    $topic_title = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_topics', 't_cache_first_title', array('id' => $topic_id));
                    if ($topic_title === null) {
                        $topic_title = '#' . strval($topic_id);
                    }
                } else {
                    $topic_title = do_lang('UNKNOWN');
                }

                $written_context = do_lang('SOMETHING_IN', '#' . $actionlog_row['param_b'], $topic_title);
                return $written_context;

            case 'DELETE_POSTS':
                $topic_id = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_posts', 'p_topic_id', array('id' => intval($actionlog_row['param_a'])));
                if ($topic_id !== null) {
                    $topic_title = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_topics', 't_cache_first_title', array('id' => $topic_id));
                    if ($topic_title === null) {
                        $topic_title = '#' . strval($topic_id);
                    }
                } else {
                    $topic_title = do_lang('UNKNOWN');
                }

                $written_context = do_lang('SOMETHING_IN', integer_format(intval($actionlog_row['param_b'])), $topic_title);
                return $written_context;

            case 'MOVE_POSTS':
                $topic_id = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_posts', 'p_topic_id', array('id' => intval($actionlog_row['param_a'])));
                if ($topic_id !== null) {
                    $topic_title = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_topics', 't_cache_first_title', array('id' => $topic_id));
                    if ($topic_title === null) {
                        $topic_title = '#' . strval($topic_id);
                    }
                } else {
                    $topic_title = do_lang('UNKNOWN');
                }

                $written_context = do_lang('SOMETHING_TO', integer_format(intval($actionlog_row['param_b'])), $topic_title);
                return $written_context;
        }

        return parent::get_written_context($actionlog_row, $handler_data, $identifier);
    }

    /**
     * Get details of action log entry types handled by this hook.
     *
     * @param  array $actionlog_row Action log row
     * @param  ?string $identifier The identifier associated with this action log entry (null: unknown / none)
     * @param  ?string $written_context The written context associated with this action log entry (null: unknown / none)
     * @param  array $bindings Default bindings
     */
    protected function get_extended_actionlog_bindings($actionlog_row, $identifier, $written_context, &$bindings)
    {
        switch ($actionlog_row['the_type']) {
            case 'DELETE_TOPIC_POLL':
                $parts = explode(':', $identifier, 2);
                if (array_key_exists(1, $parts)) {
                    $bindings += array(
                        'TOPIC' => $parts[0],
                    );
                }
                break;

            case 'EDIT_POST':
            case 'VALIDATE_POST':
            case 'UNVALIDATE_POST':
            case 'MAKE_ANONYMOUS_POST':
                $topic_id = $GLOBALS['FORUM_DB']->query_select_value_if_there('f_posts', 'p_topic_id', array('id' => intval($identifier)));
                if ($topic_id !== null) {
                    $bindings += array(
                        'TOPIC' => strval($topic_id),
                    );
                }
                break;
        }
    }
