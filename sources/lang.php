        $LANG_FILTER_OB = object_factory('LangFilter_' . fallback_lang());
    }
    lang_load_runtime_processing();

    require_lang('critical_error');
    require_lang('global');

    global $SEARCH__CONTENT_BITS;
    $SEARCH__CONTENT_BITS = null;
}

