/**
 * Log a file download, update the downloads counter and the download bandwidth counter.
 *
 * @param  AUTO_LINK $id The ID of the download being downloaded
 * @param  integer $size The size of the download (if zero, no bandwidth will be done - zero implies either an empty file, or a remote file that doesn't affect our bandwidth)
 * @param  boolean $got_before Whether the download has been downloaded before
 */
function log_download($id, $size, $got_before)
{
    // Log
    if (!$got_before) {
        $GLOBALS['SITE_DB']->query_insert('download_logging', array('id' => $id, 'member_id' => get_member(), 'ip' => get_ip_address(), 'date_and_time' => time()), false, true); // Suppress errors in case of race condition
    }

    // Update download count
    $GLOBALS['SITE_DB']->query('UPDATE ' . get_table_prefix() . 'download_downloads SET num_downloads=(num_downloads+1) WHERE id=' . strval($id), 1, 0, true); // Suppress errors in case DB access lost

    // Update stats
    $GLOBALS['SITE_DB']->query('UPDATE ' . get_table_prefix() . 'values SET the_value=' . db_cast('(' . db_cast('the_value', 'INT') . '+1)', 'CHAR') . ' WHERE the_name=\'num_downloads_downloaded\'', 1, 0, true); // Suppress errors in case DB access lost
    if ($size != 0) {
        $GLOBALS['SITE_DB']->query('UPDATE ' . get_table_prefix() . 'values SET the_value=' . db_cast('(' . db_cast('the_value', 'INT') . '+' . strval($size) . ')', 'CHAR') . ' WHERE the_name=\'download_bandwidth\'', 1, 0, true); // Suppress errors in case DB access lost
    }
}

