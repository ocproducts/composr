    if (function_exists('mixed')) {
        $WEBSTANDARDS_CHECKER_OFF = mixed();
    }
    $WEBSTANDARDS_CHECKER_OFF = null;
    $WELL_FORMED_ONLY = $well_formed_only;
    if (!$WELL_FORMED_ONLY) {
        if (function_exists('require_code')) {
