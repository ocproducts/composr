 */
function escape_html($string)
{
    //if ($string === '') return $string; // Optimisation, but doesn't work well
    if (isset($string->codename)/*faster than is_object*/) {
        return $string;
    }

    /*if ($GLOBALS['XSS_DETECT']) {  Useful for debugging
