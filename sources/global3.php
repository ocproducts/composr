        $page = filter_naughty($page);
    }
    if ($page !== '') {
        $PAGE_NAME_CACHE = str_replace('-', '_', $page); // Temporary, good enough for site.php to finish loading
    }
    $page = fix_page_name_dashing(get_zone_name(), $page);
    if ($ZONE !== null) {
        $PAGE_NAME_CACHE = $page;
    }
    $GETTING_PAGE_NAME = false;
    return $page;
}

