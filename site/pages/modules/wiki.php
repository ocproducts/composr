    /**
     * Get markers of posts from the POST/GET.
     *
     * @return array A list of markers.
     */
    public function get_markers()
    {
        $markers = array();
        foreach (array_keys($_REQUEST) as $key) {
            if (substr($key, 0, 5) == 'mark_') {
                $markers[] = intval(substr($key, 5));
            }
        }
        return $markers;
    }

